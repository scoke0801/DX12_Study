void CGrassObject::Animate(float fDeltaTime)
{
	if (m_fRotationAngle <= -1.5f) m_fRotationDelta = 1.0f;
	if (m_fRotationAngle >= +1.5f) m_fRotationDelta = -1.0f;
	m_fRotationAngle += m_fRotationDelta * fDeltaTime;

	Rotate(0.0f, 0.0f, m_fRotationAngle);
}

